    private void txtUsuarioFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtUsuarioFocusGained
        if (txtUsuario.getText().equals("Usuario")) {
            txtUsuario.setText("");
            txtUsuario.setForeground(new java.awt.Color(51, 51, 51)); // Letra oscura al escribir
}
    }//GEN-LAST:event_txtUsuarioFocusGained

    private void txtUsuarioFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtUsuarioFocusLost
        if (txtUsuario.getText().isEmpty()) {
            txtUsuario.setText("Usuario");
            txtUsuario.setForeground(new java.awt.Color(153, 153, 153)); // Vuelve al gris claro
}
    }//GEN-LAST:event_txtUsuarioFocusLost

    private void pwdContrasenaFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_pwdContrasenaFocusGained
        if (String.valueOf(pwdContrasena.getPassword()).equals("Contraseña")) {
            pwdContrasena.setText("");
            pwdContrasena.setForeground(new java.awt.Color(51, 51, 51));
            pwdContrasena.setEchoChar('*'); // Pone los asteriscos
}
    }//GEN-LAST:event_pwdContrasenaFocusGained

    private void pwdContrasenaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_pwdContrasenaFocusLost
        if (String.valueOf(pwdContrasena.getPassword()).isEmpty()) {
            pwdContrasena.setText("Contraseña");
            pwdContrasena.setForeground(new java.awt.Color(153, 153, 153));
            pwdContrasena.setEchoChar((char) 0); // Quita los asteriscos para mostrar la palabra
}
    }//GEN-LAST:event_pwdContrasenaFocusLost

